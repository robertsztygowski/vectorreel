"""Phase 0.2 deliverable 2 — publishable output.md per corpus video (PLAN.md).

These are the first artifacts vectorreel has ever produced that it is ALLOWED TO SHOW ANYONE:
every source is CC BY, verified by 08_corpus.py, and every rendered file carries its
attribution. They are the raw material for the Phase 0.3 artifact post — the long pole.

Two modes:
  hero  — the FOSDEM talk end-to-end: N x 600 s Stage B segments (server-side offsets, no
          bytes ever held) + Stage C fusion. A complete document, not an excerpt, because
          the Phase 0.3 post needs a real one to stand next to a plain transcript.
  rest  — the other four, fused from their single 10-minute segment. Enough to show the
          shape of the output per category.

Usage:  python 10_render.py hero | rest | all
"""

import datetime
import json
import sys
from importlib import import_module

from common import (OUT, gemini_call, hhmmss, ledger_total, response_text, spike_budget,
                    spike_spent)

stage_b = import_module("03_stage_b")
fusion = import_module("06_fusion")

SPIKE_CAP_USD = 3.72  # EUR 3.20: the EUR 3 envelope plus a founder-approved top-up to
                      # finish the last two renders after adaptive halving overran it

SEGMENT_S = 600
OVERLAP_S = 20  # the fusion prompt expects ~20 s of overlap so it can dedupe block-wise
RESOLUTION = "MEDIA_RESOLUTION_LOW"

HERO = "JvbBFwlqxeI"  # FOSDEM — How (Not) To Containerise Securely (29:26)

MD_DIR = OUT / "corpus_md"
MD_DIR.mkdir(exist_ok=True)


def _corpus() -> dict[str, dict]:
    data = json.loads((OUT / "corpus.json").read_text(encoding="utf-8"))
    return {v["video_id"]: v for v in data["accepted"]}


def _segments_for(v: dict) -> list[tuple[int, int]]:
    """Offset windows covering the whole video, overlapping so fusion can dedupe."""
    out, start = [], 0
    while start < v["duration_s"]:
        end = min(start + SEGMENT_S, v["duration_s"])
        out.append((start, end))
        if end >= v["duration_s"]:
            break
        start = end - OVERLAP_S
    return out


def _fuse(v: dict, segments: list[dict]) -> str:
    meta = {
        "source_filename": v["url"],
        "duration": hhmmss(v["duration_s"]),
        "processed_at": datetime.datetime.now(datetime.UTC).strftime("%Y-%m-%dT%H:%M:%SZ"),
        "generator": "vectorreel-experiments/001@phase0.2",
    }
    prompt = fusion.FUSION_PROMPT.format(
        segments_json=json.dumps(segments, ensure_ascii=False), **meta)
    body = {
        "contents": [{"role": "user", "parts": [{"text": prompt}]}],
        "generationConfig": {"temperature": 0.2, "maxOutputTokens": 65535,
                             "thinkingConfig": {"thinkingBudget": 4096}},
    }
    data = gemini_call(body, label=f"fuse_{v['video_id']}", config="corpus_fusion")
    md = response_text(data).strip()
    if md.startswith("```"):  # belt and braces — the prompt forbids fences
        md = md.split("\n", 1)[1].rsplit("```", 1)[0].strip()

    # Attribution is not decoration: CC BY is a licence with one condition, and this is it.
    # It ships INSIDE the artifact so it cannot be separated from the content downstream.
    return (f"{md}\n\n---\n\n"
            f"## Source & licence\n\n"
            f"Generated by **vectorreel** from a Creative Commons video. "
            f"The original remains the property of its authors.\n\n"
            f"> {v['attribution']}\n>\n"
            f"> Original video: {v['url']}\n"
            f"> Licence: Creative Commons Attribution (CC BY) — "
            f"https://creativecommons.org/licenses/by/3.0/\n")


MIN_SEGMENT_S = 120


def _stage_b_adaptive(v: dict, start: int, end: int) -> list[dict]:
    """Stage B on one window — and if the output overflows, HALVE THE WINDOW and recurse.

    🚨 The finding that forced this: a 600 s segment is NOT a safe bound on Stage B's output.
    The dense middle of the FOSDEM talk (09:40-19:40 — slide after slide of code) blew the
    65,535-token output cap even at ten minutes, having already forced the corpus window down
    from fifteen. Segment length simply does not bound output; ON-SCREEN TEXT DENSITY does,
    and that is not knowable before the call.

    So the pipeline cannot merely pick a good window — it must REACT to an overflow. Halving
    is the cheap, obviously-correct response: it needs no content analysis, it terminates, and
    the segments it produces are exactly what Stage C already knows how to fuse. Anything
    smarter (predicting density, trimming the schema) can come later; this is what makes the
    dense case work at all."""
    label = f"hero_{start}_{end}"
    if not (OUT / f"{label}.norm.json").exists():
        try:
            stage_b.run_stage_b(v["url"], start, label, config="corpus_lowres",
                                media_resolution=RESOLUTION, clip_dur_s=end - start,
                                youtube_window=(start, end), derive_clip_dur=True)
        except RuntimeError as e:
            if "maxOutputTokens" not in str(e) or end - start <= MIN_SEGMENT_S:
                raise
            mid = (start + end) // 2
            print(f"  !! {label} overflowed - splitting at {hhmmss(mid)}, retrying halves")
            return (_stage_b_adaptive(v, start, mid + OVERLAP_S // 2)
                    + _stage_b_adaptive(v, mid, end))
    return [json.loads((OUT / f"{label}.norm.json").read_text(encoding="utf-8"))]


def hero() -> None:
    v = _corpus()[HERO]
    windows = _segments_for(v)
    print(f"=== hero: {v['title']} ({hhmmss(v['duration_s'])}) — "
          f"{len(windows)} segments ===")

    segments = []
    for start, end in windows:
        segments.extend(_stage_b_adaptive(v, start, end))
        print(f"  spike spend: ${spike_spent():.4f} / ${SPIKE_CAP_USD}")

    segments.sort(key=lambda s: s["segment_start"])
    md = _fuse(v, segments)
    path = MD_DIR / f"{v['video_id']}_full.md"
    path.write_text(md, encoding="utf-8")
    print(f"\nwrote {path}  ({len(md.splitlines())} lines, "
          f"{md.count('## [')} topic sections)")


def rest() -> None:
    corpus = _corpus()
    for label_id, v in corpus.items():
        if label_id == HERO:
            continue
        label = f"c_{v['category']}_{v['video_id']}"
        norm = OUT / f"{label}.norm.json"
        if not norm.exists():
            print(f"  skip {v['video_id']} — no Stage B output (run 09_benchmark.py run)")
            continue
        seg = json.loads(norm.read_text(encoding="utf-8"))
        md = _fuse(v, [seg])
        # named an excerpt because that is what it is: the first 10 minutes, not the video
        path = MD_DIR / f"{v['video_id']}_excerpt_10min.md"
        path.write_text(md, encoding="utf-8")
        print(f"wrote {path}  ({md.count('## [')} topic sections)")
        print(f"  spike spend: ${spike_spent():.4f} / ${SPIKE_CAP_USD}")


if __name__ == "__main__":
    what = sys.argv[1] if len(sys.argv) > 1 else "all"
    spike_budget("phase-0.2-corpus", SPIKE_CAP_USD)
    if what in ("hero", "all"):
        hero()
    if what in ("rest", "all"):
        rest()
    print(f"\nspike spend: ${spike_spent():.4f}   ledger total: ${ledger_total():.4f}")
