using System.Collections.Concurrent;
using System.Diagnostics;
using System.Globalization;
using System.Security.Cryptography;
using System.Text;
using System.Text.Json;
using System.Text.Json.Serialization;
using MdReel.Core;
using MdReel.Core.Domain;
using MdReel.Core.Output;
using MdReel.Core.Pipeline.StageA;
using MdReel.Core.Pipeline.StageB;
using MdReel.Core.Providers;
using MdReel.Infrastructure.Pipeline;
using MdReel.Infrastructure.Storage;
using Microsoft.Extensions.Options;

namespace MdReel.Api.Features.PrivateProcessing;

public sealed partial class PrivatePipelineService(
    StageARunner stageA,
    StageBRunner stageB,
    ITextFuser fuser,
    IObjectStorage objectStorage,
    ICostLedger ledger,
    IOptions<GcsOptions> gcsOptions,
    IOptions<PipelineModelOptions> modelOptions,
    IWebHostEnvironment environment,
    ILogger<PrivatePipelineService> logger)
{
    private const string GeneratorId = "mdreel@0.0.0-private";

    // Private uploads are transient: the source is deleted after processing (ARCHITECTURE.md §3,
    // Stage D). This is the body of the output's "## Source & licence" section.
    private const string PrivateProvenance =
        "Generated by **mdreel** from your uploaded recording. The source video was deleted after "
        + "processing per the default retention policy.";

    // Stage B on the private path enforces boundaries via cue hints in the prompt, not by physically
    // splitting real segments (the boundary gate verdict — PLAN.md todo 1). Physical splitting is the
    // fallback only.
    private static readonly StageBOptions PrivateStageBOptions =
        StageBOptions.Default with { ForceCueBoundarySegmentation = false };

    private static readonly JsonSerializerOptions DocumentJson = new() { WriteIndented = true };

    private readonly string _outputBucket = gcsOptions.Value.OutputBucket;
    private readonly bool _meterModelCalls = modelOptions.Value.Mode != PipelineModelMode.Fake;

    private readonly ConcurrentDictionary<string, UploadState> _uploads = new(StringComparer.Ordinal);
    private readonly ConcurrentDictionary<string, JobState> _jobs = new(StringComparer.Ordinal);
    private readonly ConcurrentQueue<ErasureAuditEntry> _erasureAudit = new();
    private readonly string _uploadRoot = Path.Combine(environment.ContentRootPath, ".local-state", "uploads");

    public UploadCreatedResponse CreateUpload(HttpRequest request)
    {
        Directory.CreateDirectory(_uploadRoot);

        var uploadId = $"up_{Guid.NewGuid():N}";
        var signature = Convert.ToHexString(RandomNumberGenerator.GetBytes(16)).ToLowerInvariant();
        var targetPath = Path.Combine(_uploadRoot, $"{uploadId}.bin");

        _uploads[uploadId] = new UploadState(uploadId, signature, targetPath);

        var uploadUrl = $"{request.Scheme}://{request.Host}/api/v1/uploads/{uploadId}/content?sig={signature}";
        return new UploadCreatedResponse(uploadId, uploadUrl);
    }

    public async Task<bool> StoreUploadAsync(
        string uploadId,
        string signature,
        HttpRequest request,
        CancellationToken cancellationToken)
    {
        if (!_uploads.TryGetValue(uploadId, out var upload))
        {
            return false;
        }

        if (!string.Equals(upload.Signature, signature, StringComparison.Ordinal))
        {
            return false;
        }

        Directory.CreateDirectory(_uploadRoot);
        await using var write = File.Create(upload.Path);
        await request.Body.CopyToAsync(write, cancellationToken);
        await write.FlushAsync(cancellationToken);
        upload.Stored = true;
        return true;
    }

    public JobCreatedResponse? CreateJob(string uploadId, CreateJobOptions? options)
    {
        if (!_uploads.TryGetValue(uploadId, out var upload))
        {
            return null;
        }

        var jobId = $"job_{Guid.NewGuid():N}";
        var source = options?.Filename ?? $"{uploadId}.mp4";
        var now = DateTimeOffset.UtcNow;
        var state = new JobState
        {
            Id = jobId,
            UploadId = uploadId,
            Status = "queued",
            Stage = null,
            Progress = 0,
            Source = source,
            DurationSec = options?.DurationSec,
            CreatedAt = now,
            FinishedAt = null,
            CostCents = null,
            WallClockSec = null,
            ShouldFail = options?.Fail ?? false,
        };

        _jobs[jobId] = state;
        _ = ProcessJobAsync(state, upload);
        return new JobCreatedResponse(jobId);
    }

    public IReadOnlyList<JobListItemResponse> ListJobs() =>
        _jobs.Values
            .OrderByDescending(static x => x.CreatedAt)
            .Select(
                static x => new JobListItemResponse(
                    x.Id,
                    x.Status,
                    x.Status == "processing" ? x.Stage : null,
                    x.Progress,
                    x.Source,
                    x.DurationSec,
                    TimestampFormatter.ToUtcTimestamp(x.CreatedAt),
                    x.FinishedAt is null ? null : TimestampFormatter.ToUtcTimestamp(x.FinishedAt.Value)))
            .ToArray();

    public JobStatusResponse? GetJobStatus(string id)
    {
        if (!_jobs.TryGetValue(id, out var job))
        {
            return null;
        }

        return new JobStatusResponse(
            job.Status,
            job.Status == "processing" ? job.Stage : null,
            job.Progress,
            job.Status == "done" ? job.CostCents : null,
            job.Status == "done" ? job.DurationSec : null,
            job.Status == "done" ? job.WallClockSec : null);
    }

    public bool HasJob(string id) => _jobs.ContainsKey(id);

    public JobOutputResponse? GetOutput(string id)
    {
        if (!_jobs.TryGetValue(id, out var job))
        {
            return null;
        }

        return job.Output;
    }

    public bool DeleteJob(string id)
    {
        var removed = _jobs.TryRemove(id, out var job);
        if (!removed || job is null)
        {
            return false;
        }

        if (_uploads.TryGetValue(job.UploadId, out var upload))
        {
            TryDeleteFile(upload.Path);
        }

        _erasureAudit.Enqueue(new ErasureAuditEntry(id, DateTimeOffset.UtcNow));
        return true;
    }

    private async Task ProcessJobAsync(JobState job, UploadState upload)
    {
        var started = DateTimeOffset.UtcNow;

        // Root span per job (no parent: the HTTP request span ends at 202). jobId is the tag the
        // agent runbook searches on (TESTING.md) — one query from a red test to this trace.
        using var jobActivity = PipelineDiagnostics.Source.StartActivity(
            "pipeline.job", ActivityKind.Internal, parentContext: default);
        jobActivity?.SetTag("mdreel.job_id", job.Id);
        jobActivity?.SetTag("mdreel.upload_id", job.UploadId);
        jobActivity?.SetTag("mdreel.source", job.Source);

        try
        {
            PreparedVideo? prepared = null;
            SetProcessing(job, "A", 15);
            using (StartStage(job, "A"))
            {
                prepared = await ExecuteStageAAsync(job, upload);
            }

            if (job.ShouldFail)
            {
                SetFailed(job);
                jobActivity?.SetStatus(ActivityStatusCode.Error, "simulated failure requested");
                logger.LogWarning("Job {JobId} failed (simulated failure requested)", job.Id);
                return;
            }

            if (prepared is null)
            {
                throw new InvalidOperationException("Stage A did not produce a prepared video.");
            }

            SetProcessing(job, "B", 45);
            List<SegmentAnalysis> analyses;
            using (StartStage(job, "B"))
            {
                analyses = await ExecuteStageBAsync(job, upload, prepared);
            }

            SetProcessing(job, "C", 75);
            OutputDocument document;
            using (StartStage(job, "C"))
            {
                document = await ExecuteStageCAsync(job, prepared, analyses);
            }

            SetProcessing(job, "D", 92);
            string markdown;
            using (StartStage(job, "D"))
            {
                markdown = await ExecuteStageDAsync(job, document);
            }

            var finishedAt = DateTimeOffset.UtcNow;
            job.Status = "done";
            job.Stage = null;
            job.Progress = 100;
            job.CostCents = SumPricedCents(job.Id);
            job.WallClockSec = Math.Max(1, (finishedAt - started).TotalSeconds);
            job.FinishedAt = finishedAt;
            job.DurationSec ??= prepared.Probe.Duration.TotalSeconds;
            job.Output = BuildOutput(job.Source, document, markdown);

            TryDeleteFile(upload.Path);
            jobActivity?.SetTag("mdreel.cost_cents", job.CostCents);
            LogJobDone(job.Id, job.WallClockSec.Value, job.CostCents ?? 0);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Private pipeline failed for job {JobId}", job.Id);
            jobActivity?.SetStatus(ActivityStatusCode.Error, ex.Message);
            SetFailed(job);
        }
    }

    private Activity? StartStage(JobState job, string stage)
    {
        LogStageEnter(job.Id, stage);
        var activity = PipelineDiagnostics.Source.StartActivity($"pipeline.stage.{stage}");
        activity?.SetTag("mdreel.job_id", job.Id);
        activity?.SetTag("mdreel.stage", stage);
        return activity;
    }

    [LoggerMessage(Level = LogLevel.Information, Message = "Job {JobId} entering stage {Stage}")]
    private partial void LogStageEnter(string jobId, string stage);

    [LoggerMessage(Level = LogLevel.Information, Message = "Job {JobId} done in {WallClockSec}s (cost {CostCents}c)")]
    private partial void LogJobDone(string jobId, double wallClockSec, int costCents);

    private async Task<PreparedVideo> ExecuteStageAAsync(JobState job, UploadState upload)
    {
        if (!upload.Stored || !File.Exists(upload.Path))
        {
            throw new InvalidOperationException($"Upload {job.UploadId} has no stored bytes to process.");
        }

        var prepared = await stageA.PrepareAsync(job.Id, upload.Path, StageAOptions.Default, CancellationToken.None);
        job.DurationSec ??= prepared.Probe.Duration.TotalSeconds;
        return prepared;
    }

    private async Task<List<SegmentAnalysis>> ExecuteStageBAsync(JobState job, UploadState upload, PreparedVideo prepared)
    {
        // Fake mode ignores the URI; Live/Record need a source the model can fetch. Until the private
        // path stages raw bytes into GCS for Vertex, the local path is the source identifier.
        var sourceUri = upload.Path;

        List<SegmentAnalysis> analyses = [];
        foreach (var segment in prepared.Segments)
        {
            var analyzed = await stageB.AnalyzeAsync(
                sourceUri,
                segment,
                StageAOptions.Default,
                PrivateStageBOptions,
                CancellationToken.None);
            analyses.AddRange(analyzed);
            MeterModelCall(job.Id, "stage_b.analyze");
        }

        return [.. analyses.OrderBy(static x => x.SegmentStart).ThenBy(static x => x.SegmentIndex)];
    }

    private async Task<OutputDocument> ExecuteStageCAsync(JobState job, PreparedVideo prepared, IReadOnlyList<SegmentAnalysis> analyses)
    {
        var request = new FusionRequest(
            Source: job.Source,
            Duration: prepared.Probe.Duration,
            Provenance: PrivateProvenance,
            Generator: GeneratorId);

        var document = await fuser.FuseAsync(analyses, request, CancellationToken.None);
        MeterModelCall(job.Id, "stage_c.fuse");
        return document;
    }

    private async Task<string> ExecuteStageDAsync(JobState job, OutputDocument document)
    {
        var markdown = OutputMarkdownRenderer.Render(document);
        var prefix = $"private/{job.Id}";

        await WriteUtf8Async(_outputBucket, $"{prefix}/output.md", markdown, CancellationToken.None);
        await WriteUtf8Async(
            _outputBucket,
            $"{prefix}/output.json",
            JsonSerializer.Serialize(document, DocumentJson),
            CancellationToken.None);

        return markdown;
    }

    private async Task WriteUtf8Async(string bucket, string objectName, string content, CancellationToken cancellationToken)
    {
        await using var stream = new MemoryStream(Encoding.UTF8.GetBytes(content));
        await objectStorage.WriteAsync(bucket, objectName, stream, cancellationToken);
    }

    // Every LLM call is recorded in the ledger (CLAUDE.md rule 6). Fake mode makes no model call, so
    // nothing is recorded — the E2E ledger stays exactly Stage A's two compute steps. Cents are left
    // null: a laptop cannot know the Vertex rate, and a fabricated price is worse than an honest gap
    // (mirrors Stage A — StageARunner.Meter).
    private void MeterModelCall(string jobId, string step)
    {
        if (!_meterModelCalls)
        {
            return;
        }

        ledger.Record(new CostEntry(
            JobId: jobId,
            Kind: CostKind.Llm,
            Step: step,
            Quantity: 1,
            Unit: "calls",
            Cents: null));
    }

    private int? SumPricedCents(string jobId)
    {
        if (ledger is not InMemoryCostLedger inMemory)
        {
            return null;
        }

        var cents = inMemory.Entries
            .Where(e => string.Equals(e.JobId, jobId, StringComparison.Ordinal) && e.Cents.HasValue)
            .Sum(e => e.Cents!.Value);
        return cents;
    }

    private static JobOutputResponse BuildOutput(string sourceFilename, OutputDocument document, string markdown)
    {
        var outputFilename = $"{Path.GetFileNameWithoutExtension(sourceFilename)}.md";
        return new JobOutputResponse(outputFilename, markdown, document);
    }

    private static void SetProcessing(JobState job, string stage, double progress)
    {
        job.Status = "processing";
        job.Stage = stage;
        job.Progress = progress;
    }

    private static void SetFailed(JobState job)
    {
        job.Status = "failed";
        job.Stage = null;
        job.Progress = 100;
        job.FinishedAt = DateTimeOffset.UtcNow;
    }

    private static void TryDeleteFile(string path)
    {
        if (File.Exists(path))
        {
            File.Delete(path);
        }
    }
}

public sealed record UploadCreatedResponse(
    [property: JsonPropertyName("uploadId")] string UploadId,
    [property: JsonPropertyName("uploadUrl")] string UploadUrl);

